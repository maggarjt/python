class Fighters:
		# class
	def __init__(self, name ):
		self.name = name
		# name is an attribute of class Fighters
		# __init__ is a method of class Fighters
qazi = Fighters("Qazi")
bob = Fighters("Cat")
		# qazi is an object instantiated from class Fighters
print(qazi.name)
print(bob.name)
		# name is an attribute of object bob which was instantiated from Class Fighters

